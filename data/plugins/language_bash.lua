-- mod-version:4
local syntax = require "core.syntax"

syntax.add {
  name = "Bash",
  files = { "%.sh$", "%.bash$", "%.zsh$", "%.fish$", "%.bashrc$", "%.zshrc$" },
  headers = "^#!.*[ /][bz]?a?sh",
  comment = "#",
  patterns = {
    { pattern = "#.*",                              type = "comment"  },
    { pattern = { '"', '"', '\\' },                 type = "string"   },
    { pattern = { "'", "'" },                       type = "string"   },
    { pattern = "<<%-?%s*'?[%a_][%w_]*'?",          type = "keyword"  },
    { pattern = "%$[%a_@#%*%?%-!0-9][%w_]*",        type = "keyword2" },
    { pattern = "%${[^}]*}",                         type = "keyword2" },
    { pattern = "%d+",                              type = "number"   },
    { pattern = "[|&;<>%(%)%[%]%!]",               type = "operator" },
    { pattern = "=",                                type = "operator" },
    { pattern = "%$%(",                             type = "operator" },
    { pattern = "[%a_][%w_%-%.]*%f[%(]",            type = "function" },
    { pattern = "[%a_][%w_%-]*",                    type = "symbol"   },
  },
  symbols = {
    ["if"]       = "keyword",
    ["then"]     = "keyword",
    ["else"]     = "keyword",
    ["elif"]     = "keyword",
    ["fi"]       = "keyword",
    ["for"]      = "keyword",
    ["while"]    = "keyword",
    ["until"]    = "keyword",
    ["do"]       = "keyword",
    ["done"]     = "keyword",
    ["in"]       = "keyword",
    ["case"]     = "keyword",
    ["esac"]     = "keyword",
    ["function"] = "keyword",
    ["return"]   = "keyword",
    ["local"]    = "keyword",
    ["export"]   = "keyword",
    ["readonly"] = "keyword",
    ["declare"]  = "keyword",
    ["typeset"]  = "keyword",
    ["unset"]    = "keyword",
    ["shift"]    = "keyword",
    ["break"]    = "keyword",
    ["continue"] = "keyword",
    ["exit"]     = "keyword",
    ["source"]   = "keyword",
    ["exec"]     = "keyword",
    ["set"]      = "keyword",
    ["trap"]     = "keyword",
    ["echo"]     = "function",
    ["printf"]   = "function",
    ["read"]     = "function",
    ["test"]     = "function",
    ["true"]     = "literal",
    ["false"]    = "literal",
  },
}
