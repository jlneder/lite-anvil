  name = "Julia",
  files = { "%.jl$" },
  comment = "#",
  block_comment = { "#=", "=#" },
  patterns = {
    { pattern = "#.*",                               type = "comment"  },
    { pattern = { "#=", "=#" },                      type = "comment"  },
    { pattern = { "\"\"\"", "\"\"\"", '\\' },       type = "string"   },
    { pattern = { '"', '"', '\\' },                  type = "string"   },
